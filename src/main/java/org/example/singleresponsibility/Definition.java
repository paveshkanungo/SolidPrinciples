package org.example.singleresponsibility;

public class Definition {
    void singleReponsibilityDefinition(){
        // Single Responsibility Principle(SRP) means that a class should have ONE and ONLY ONE job or responsibility.
        // Meaning, if the class has multiple jobs or responsibilities, changes to one responsibility
        // might affect or break the other critical responsibilities, making the code bloated, fragile,
        // and harder to maintain. Hence, the Single Responsibility Principle(SRP) focuses on restricting
        // the concern to only a single responsibility.
    }
}
